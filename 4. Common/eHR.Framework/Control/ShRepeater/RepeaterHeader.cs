using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace eHR.Framework.Control.ShRepeater
{
    public class RepeaterHeader
    {
        public RepeaterHeader() { }
        public RepeaterHeader(string headerName, string headerCssClass, string headerWidth)
        {
            this.HeaderName = headerName;
            this.HeaderCssClass = headerCssClass;
            this.HeaderWidth = headerWidth;
        }
        private string _headerName;
        private string _headerCssClass;
        private string _headerWidth;

        public string HeaderName
        {
            get { return _headerName; }
            set { _headerName = value; }
        }
        public string HeaderCssClass
        {
            get { return _headerCssClass; }
            set { _headerCssClass = value; }
        }
        public string HeaderWidth
        {
            get { return _headerWidth; }
            set { _headerWidth = value; }
        }

    }
}
